package com.swu.wechat.entity;

public class Message {
   private String from;
   private String to;
   private String msg;
   private String date;
public String getFrom() {
	return from;
}
public void setFrom(String from) {
	this.from = from;
}
public String getTo() {
	return to;
}
public void setTo(String to) {
	this.to = to;
}
public String getMsg() {
	return msg;
}
public void setMsg(String msg) {
	this.msg = msg;
}
public String getDate() {
	return date;
}
public void setDate(String date) {
	this.date = date;
}
   
}
